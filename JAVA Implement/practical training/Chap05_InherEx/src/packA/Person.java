package packA;

public class Person {
	private float weight;
	int age;
	protected float height;
	public String name;
	
	public void setWeight(float weight){
		this.weight = weight;
	}
	
	public float getWeight(){
		return weight;
	}
	
	public void setAge(int age){
		this.age = age;
	}
	
	public int getAge(){
		return age;
	}
}
